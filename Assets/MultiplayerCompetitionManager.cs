using UnityEngine;
using System.IO;
using System.Collections.Generic;

public class MultiplayerCompetitionManager : MonoBehaviour
{
    public string actualCompetition;    
    public List<string> competitions;

    public const string FOLDER = "competencias";

    void Start()
    {
        string folder = FOLDER + "/";
        var info = new DirectoryInfo(folder);
        var fileInfo = info.GetDirectories();

        foreach (DirectoryInfo fileData in fileInfo)
        {            
            string fileName = fileData.Name;
            competitions.Add(fileName);
        }
    }
    
}
